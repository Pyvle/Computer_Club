WITH target_floorplans AS (
    SELECT DISTINCT fp.id, fp.name, c.name AS club_name
    FROM club_floorplans fp
    JOIN clubs c ON c.id = fp.club_id
    JOIN club_staff cs ON cs.club_id = c.id
    JOIN users u ON u.id = cs.user_id
    WHERE u.phone = '+79991234567'
), item_json AS (
    SELECT
        tf.id AS floorplan_id,
        jsonb_agg(
            CASE i.item_type
                WHEN 'FLOOR' THEN jsonb_build_object(
                    'type', 'FLOOR',
                    'col', i.col_no,
                    'row', i.row_no,
                    'roomType', i.room_type
                )
                WHEN 'WALL' THEN jsonb_build_object(
                    'type', 'WALL',
                    'orientation', i.orientation,
                    'col', i.col_no,
                    'row', i.row_no,
                    'auto', i.auto_wall
                )
                WHEN 'SEAT' THEN jsonb_build_object(
                    'type', 'SEAT',
                    'seatId', s.id,
                    'col', i.col_no,
                    'row', i.row_no
                )
            END
            ORDER BY i.item_order
        ) FILTER (
            WHERE i.item_type <> 'SEAT' OR s.id IS NOT NULL
        ) AS items
    FROM target_floorplans tf
    JOIN demo_owner_floorplan_items i
        ON i.club_name = tf.club_name
       AND i.floorplan_name = tf.name
    LEFT JOIN clubs c ON c.name = i.club_name
    LEFT JOIN seats s
        ON s.club_id = c.id
       AND s.label = i.seat_label
    GROUP BY tf.id
), dimensions AS (
    SELECT fp.id, MAX(i.col_no) + 2 AS cols, MAX(i.row_no) + 2 AS rows
    FROM club_floorplans fp
    JOIN item_json ij ON ij.floorplan_id = fp.id
    JOIN clubs c ON c.id = fp.club_id
    JOIN demo_owner_floorplan_items i
        ON i.club_name = c.name
       AND i.floorplan_name = fp.name
    GROUP BY fp.id
)
UPDATE club_floorplans fp
SET width = dimensions.cols * 32,
    height = dimensions.rows * 32,
    grid_size = 32,
    version = fp.version + 1,
    data = jsonb_build_object('version', fp.version + 1, 'items', COALESCE(item_json.items, '[]'::jsonb)),
    updated_at = localtimestamp
FROM item_json
JOIN dimensions ON dimensions.id = item_json.floorplan_id
WHERE fp.id = item_json.floorplan_id;
